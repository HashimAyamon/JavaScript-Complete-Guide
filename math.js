// 1. Getting the Value of Pi
console.log("Pi:", Math.PI); // 3.141592653589793

// 2. Rounding Numbers
console.log("Round 4.7:", Math.round(4.7)); // 5
console.log("Round 4.3:", Math.round(4.3)); // 4

// 3. Getting the Ceiling and Floor Values
console.log("Ceil 4.3:", Math.ceil(4.3)); // 5 (rounds up)
console.log("Floor 4.7:", Math.floor(4.7)); // 4 (rounds down)

// 4. Getting the Absolute Value
console.log("Absolute of -10:", Math.abs(-10)); // 10

// 5. Power and Square Root
console.log("2^3 (2 to the power of 3):", Math.pow(2, 3)); // 8
console.log("Square root of 25:", Math.sqrt(25)); // 5

// 6. Getting Maximum and Minimum
console.log("Max of (10, 20, 5, 30):", Math.max(10, 20, 5, 30)); // 30
console.log("Min of (10, 20, 5, 30):", Math.min(10, 20, 5, 30)); // 5

// 7. Generating a Random Number
console.log("Random Number (0 to 1):", Math.random()); // Random value between 0 and 1
console.log("Random Number (1 to 100):", Math.floor(Math.random() * 100) + 1);

// 8. Trigonometric Functions
console.log("Sin(90 degrees):", Math.sin(Math.PI / 2)); // 1
console.log("Cos(0 degrees):", Math.cos(0)); // 1
console.log("Tan(45 degrees):", Math.tan(Math.PI / 4)); // 1